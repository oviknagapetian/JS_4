"use strict";

/*
Используя Math.random() необходимо сгенерировать массив, содержащий 5 цифр в 
диапазоне [0, 9].
После создания массива необходимо вывести в консоль следующие значения:
1. Сумму элементов массива
2. Минимальное значение в массиве
3. Новый массив, содержащий индексы сгенерированного выше массива, в которых 
значение равно 3.
Пример: Если у нас сгенерировался массив [2, 3, 5, 7, 3], то мы должны вывести 
в консоль массив [1, 4]. Такой массив получился потому что в сгенерированном
массиве тройки лежат под индексами 1 и 4. Если троек в сгенерированном массиве
не окажется, значит нужно будет вывести пустой массив.
*/


// Создаем пустой массив для хранения случайно сгенерированных цифр
const randomArray = [];

// Заполняем массив случайно сгенерированными цифрами от 0 до 9
for (let i = 0; i < 5; i++) {
    // Генерируем случайное число от 0 до 9 и добавляем его в массив
    randomArray.push(Math.floor(Math.random() * 10));
}

// Выводим сгенерированный массив в консоль
console.log("Сгенерированный массив:", randomArray);

// Вычисляем сумму элементов массива
const sum = randomArray.reduce((acc, curr) => acc + curr, 0);
console.log("Сумма элементов массива:", sum);

// Находим минимальное значение в массиве
const min = Math.min(...randomArray);
console.log("Минимальное значение в массиве:", min);

// Создаем новый массив, содержащий индексы элементов равных 3
const indexesOfThree = [];
randomArray.forEach((element, index) => {
    if (element === 3) {
        indexesOfThree.push(index);
    }
});

// Выводим массив индексов, содержащих тройки
console.log("Индексы элементов равных 3:", indexesOfThree);
